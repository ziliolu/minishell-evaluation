#include "../../../includes/minishell.h"

void	ft_free_array(char **array)
{
	int	i;

	i = 0;
	if (!array)
		return ;
	while (array[i])
	{
		if (array[i])
			free (array[i++]);
	}
	if (array)
		free (array);
}
